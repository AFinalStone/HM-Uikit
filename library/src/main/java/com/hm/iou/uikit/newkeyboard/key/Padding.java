package com.hm.iou.uikit.newkeyboard.key;

public class Padding {
    int top;
    int left;
    int bottom;
    int right;

    /**
     * px
     *
     * @param top
     * @param left
     * @param bottom
     * @param right
     */
    public Padding(int top, int left, int bottom, int right) {
        this.top = top;
        this.left = left;
        this.bottom = bottom;
        this.right = right;
    }
}